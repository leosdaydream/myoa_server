package com.chinapower.entity;

public class Press {
	private int press_id;
	private String press_title;
	private String press_content;
	private String press_create_time;
	private String press_auth;
	private String press_image_path;
	public int getPress_id() {
		return press_id;
	}
	public void setPress_id(int press_id) {
		this.press_id = press_id;
	}
	public String getPress_title() {
		return press_title;
	}
	public void setPress_title(String press_title) {
		this.press_title = press_title;
	}
	public String getPress_content() {
		return press_content;
	}
	public void setPress_content(String press_content) {
		this.press_content = press_content;
	}
	public String getPress_create_time() {
		return press_create_time;
	}
	public void setPress_create_time(String press_create_time) {
		this.press_create_time = press_create_time;
	}
	public String getPress_auth() {
		return press_auth;
	}
	public void setPress_auth(String press_auth) {
		this.press_auth = press_auth;
	}
	public String getPress_image_path() {
		return press_image_path;
	}
	public void setPress_image_path(String press_image_path) {
		this.press_image_path = press_image_path;
	}
	

}
