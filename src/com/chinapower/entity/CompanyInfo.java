package com.chinapower.entity;

public class CompanyInfo {
     private Integer id;
     private String company_name;
     private Integer company_id;
     private String company_address;
     private String company_descrption;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getCompany_name() {
		return company_name;
	}
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	public Integer getCompany_id() {
		return company_id;
	}
	public void setCompany_id(Integer company_id) {
		this.company_id = company_id;
	}
	public String getCompany_address() {
		return company_address;
	}
	public void setCompany_address(String company_address) {
		this.company_address = company_address;
	}
	public String getCompany_descrption() {
		return company_descrption;
	}
	public void setCompany_descrption(String company_descrption) {
		this.company_descrption = company_descrption;
	}
     
}
